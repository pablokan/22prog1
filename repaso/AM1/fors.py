lista = ["uno", "dos", "tres"]

# recorrido por índice
for i in range(len(lista)): # para cada índice (posición)
    print(lista[i]) # imprima el elemento de dicha posición

# recorrido por elemento
for elemento in lista: # para cada elemento de la lista, haga lo que sigue
    print(elemento) # imprima elemento

nombres = ["Juan", "Ana", "Pedro"]
for nombre in nombres:
    print(nombre)


